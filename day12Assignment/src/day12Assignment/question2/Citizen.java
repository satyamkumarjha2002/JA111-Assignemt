package day12Assignment.question2;

public class Citizen {
	String nameString;
	String aadharNumberString;
	String mobileNumberString;
	public String getNameString() {
		return nameString;
	}
	public void setNameString(String nameString) {
		this.nameString = nameString;
	}
	public String getAadharNumberString() {
		return aadharNumberString;
	}
	public void setAadharNumberString(String aadharNumberString) {
		this.aadharNumberString = aadharNumberString;
	}
	public String getMobileNumberString() {
		return mobileNumberString;
	}
	public void setMobileNumberString(String mobileNumberString) {
		this.mobileNumberString = mobileNumberString;
	}
}
